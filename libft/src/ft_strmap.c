#include "libft.h"

char	*ft_strmap(char const *s, char (*f)(char))
{
	char	*str;
	int		len;
	int		i;

	if (s == 0 || f == 0)
		return (NULL);
	len = ft_strlen(s);
	str = (char *)malloc((sizeof(char)) * (len + 1));
	i = 0;
	while (s[i])
	{
		str[i] = f(s[i]);
		i++;
	}
	str[i] = '\0';
	return (str);
}
